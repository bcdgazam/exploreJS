/*Arithmetic Operators
+	Addition
-	Subtraction
*	Multiplication
**	Exponentiation (ES2016)
/	Division
%	Modulus (Division Remainder)
++	Increment
--	Decrement */


var number1 = 21;
var number2 = 10;

var total = number1 + number2;
console.log(total);

var total2 = number1 - number2;
console.log(total2);

var total3 = number1 * number2;
console.log(total3);

var total4 = number1 / number2;
console.log(total4);

var total5 = number1 % number2;
console.log(total5);

number1 ++;
console.log(number1);

number2 --;
console.log(number2);

